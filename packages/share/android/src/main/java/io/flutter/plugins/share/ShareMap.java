package io.flutter.plugins.share;

public class ShareMap {
    String text;
    String subject;

    public ShareMap() {
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getSubject() {
        return subject;
    }

    public void setSubject(String subject) {
        this.subject = subject;
    }
}
